package proektt_13;

public class Vehiclee {
	    private String color;

	    // Constructor with a default color
	    Vehiclee() {
	        color = "Blue"; // Set a default color, e.g., "Blue"
	    }

	    // Constructor with a parameter to set the color
	    Vehiclee(String a) {
	        color = a;
	    }
	    public String toString() {
	    return color;
	}
}

